namespace Lumora.Core.Math;

/// <summary>
/// Type aliases for Lumora math system.
/// Lumora math uses both lowercase (float2, float3) and capitalized (Float2, Float3) versions.
/// This file provides aliases to support both naming conventions.
/// </summary>

// Capitalized vector type aliases
using Float2 = float2;
using Float3 = float3;
using Float4 = float4;
using Float4x4 = float4x4;
using FloatQ = floatQ;

// Color type aliases
using ColorX = color;
using Color = color;
